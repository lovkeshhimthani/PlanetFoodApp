    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try{
            String str=jcCat.getSelectedItem().toString();
            String str1=txtCatName.getText();
             if(str1.isEmpty()){
            JOptionPane.showMessageDialog(null,"Any of the fields can't be left blank!","Error",JOptionPane.ERROR_MESSAGE);
            return;
            }
            boolean ans=CategoryDao.editCategory(str,str1);
            if(ans==true){
                 JOptionPane.showMessageDialog(null,"Category Edited","Succeess",JOptionPane.INFORMATION_MESSAGE);
                 int res=JOptionPane.showConfirmDialog(null,"Do you want to edit other category?","Add Category",JOptionPane.YES_NO_OPTION);
                 if(res==JOptionPane.YES_OPTION){
                  clearAll(); 
                 }
                 else{
                     AdminOptionsFrame adf=new AdminOptionsFrame();
                     adf.setVisible(true);
                     this.dispose();
                 }
            }
            else{
                 JOptionPane.showMessageDialog(null,"Catgory can't be Edited","Failure!",JOptionPane.ERROR_MESSAGE);
                 clearAll();
            }  
     
        }
         catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"DB Error","Edit Category Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
         }
        
    }//GEN-LAST:event_btnEditActionPerformed

