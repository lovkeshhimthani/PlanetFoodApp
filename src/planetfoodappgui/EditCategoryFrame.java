    private void loadCategory() {
           try{
        map=CategoryDao.getAllCategoryId();
        if(map.size()==0){
            JOptionPane.showMessageDialog(null,"No Category to be edited!","Empty",JOptionPane.INFORMATION_MESSAGE);
             AdminOptionsFrame adf=new AdminOptionsFrame();
            adf.setVisible(true);
            this.dispose();
        }
        x=0;
        jcCat.removeAllItems();
        Set<String>name=map.keySet();
        for(String k:name){
         jcCat.addItem(map.get(k));
         x++;
       }   
    }
     catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"DB Error","Edit Category Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
         }
    }
