    public boolean validateInputs(){
        eid=txtEid.getText();
        ename=txtEname.getText();
        job=jcJob.getSelectedItem().toString();
        sal=Double.parseDouble(txtSal.getText());
        if(eid.isEmpty()||ename.isEmpty()||jcJob.getSelectedIndex()==0||txtSal.getText().isEmpty()){
            return false;
        }
        return true;
    }
