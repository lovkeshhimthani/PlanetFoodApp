    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        boolean result=validateInputs();
        if(!result){
            JOptionPane.showMessageDialog(null,"Any of the fields can't be left blank!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            Employee e=new Employee();
            sal=Double.parseDouble(txtSal.getText());
            e.setEmpid(eid);
            e.setJob(job);
            e.setSal(sal);
            e.seteName(ename);
            
            boolean ans=EmployeeDao.addEmployee(e);
            if(ans==true){
                 JOptionPane.showMessageDialog(null,"Employee Added","Succeess",JOptionPane.INFORMATION_MESSAGE);
                 int res=JOptionPane.showConfirmDialog(null,"Do you want to add other Employee?","Add Employee",JOptionPane.YES_NO_OPTION);
                 if(res==JOptionPane.YES_OPTION){
                  clearAll();
                 }
                 else{
                     AdminOptionsFrame adf=new AdminOptionsFrame();
                     adf.setVisible(true);
                     this.dispose();
                 }
            }
            else{   
             JOptionPane.showMessageDialog(null,"Employee can't be added","Failure!",JOptionPane.ERROR_MESSAGE);
             clearAll();
            }
        }
         catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"DB Error","Add Employee Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
         }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Salary should contain only numbers","Edit Employee Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

