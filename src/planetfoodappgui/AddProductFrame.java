    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        boolean result=validateInputs();
        if(!result){
            JOptionPane.showMessageDialog(null,"Any of the fields can't be left blank!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            Product p=new Product();
            proprice=Double.parseDouble(txtProPrice.getText());
            p.setCatid(map.get(jcCategory.getSelectedItem().toString()));
            p.setProdName(proname);
            p.setProdid(procode);
            p.setProdPrice(proprice);
            p.setIsActive("Y");
            boolean ans=ProductDao.addProduct(p);
            if(ans==true){
                 JOptionPane.showMessageDialog(null,"Product Added","Succeess",JOptionPane.INFORMATION_MESSAGE);
                 int res=JOptionPane.showConfirmDialog(null,"Do you want to add other product?","Add Product",JOptionPane.YES_NO_OPTION);
                 if(res==JOptionPane.YES_OPTION){
                  clearAll();
                 }
                 else{
                     AdminOptionsFrame adf=new AdminOptionsFrame();
                     adf.setVisible(true);
                     this.dispose();
                 }
            }
            else{
                 JOptionPane.showMessageDialog(null,"Product can't be added","Failure!",JOptionPane.ERROR_MESSAGE);
                 clearAll();
            }
        }
          catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"DB Error","Add product Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
         }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Product price should contain only numbers","Add product Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddProductActionPerformed

