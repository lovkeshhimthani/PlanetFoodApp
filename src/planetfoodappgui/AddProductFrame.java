public void showCategories(){
    try{
        map=CategoryDao.getAllCategoryId();
        jcCategory.removeAllItems();
        Set<String>name=map.keySet();
        jcCategory.addItem(" ");
        for(String x:name){
         jcCategory.addItem(x);
       }   
       String pid=ProductDao.getNewId();
       txtProCode.setText(pid);
    }
     catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"DB Error","Login Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
         }
}
