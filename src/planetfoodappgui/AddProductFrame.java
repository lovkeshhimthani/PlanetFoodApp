    private boolean validateInputs() {
        procode=txtProCode.getText();
        proname=txtProName.getText();
        txtProPrice.getText();
        if(procode.isEmpty()||txtProPrice.getText().isEmpty()||jcCategory.getSelectedIndex()==0||proname.isEmpty()){
            return false;
        }
        return true;
    }

    private void clearAll() {
        jcCategory.setSelectedIndex(0);
        txtProCode.setText("");
        txtProName.setText("");
        txtProPrice.setText("");
        showCategories();
    }
